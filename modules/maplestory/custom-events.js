/**
 * modules/maplestory/custom-events.js — 3 custom server events for MapleStory Cosmic.
 *
 * Creates event scripts in workspace/Cosmic/scripts/event/ using the OdinMS
 * event script API (em object + Java interop). These are server-wide boost events
 * that can be triggered by GMs or on a schedule.
 *
 * Events:
 *   CosmicExpWeekend  — Doubles the server EXP rate for a weekend event
 *   CosmicDropFest    — Doubles the server drop rate to celebrate milestones
 *   CosmicGoldRush    — Doubles the server meso rate to help new players gear up
 *
 * All 3 follow the same pattern seen in 2xEvent.js:
 *   - init() loads the event
 *   - start() activates the boost and broadcasts a world announcement
 *   - stop() reverts the boost and broadcasts a "ended" message
 *   - All required OdinMS event hooks have no-op stubs
 *
 * Idempotent — skips writing if script already exists.
 */

