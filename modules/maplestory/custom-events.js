// ── Deploy ────────────────────────────────────────────────────────────────────

/**
 * Deploy all 3 custom event scripts to workspace/Cosmic/scripts/event/.
 * Idempotent — skips existing scripts.
 */
export function deployCustomEvents() {
  if (!existsSync(EVENT_DIR)) {
    mkdirSync(EVENT_DIR, { recursive: true });
    log.info({ dir: EVENT_DIR }, 'Created event scripts directory');
  }

  const results = [];

  for (const event of CUSTOM_EVENTS) {
    const filePath = join(EVENT_DIR, `${event.name}.js`);

    if (existsSync(filePath)) {
      log.debug({ name: event.name }, 'Event script already exists, skipping');
      results.push({ name: event.name, title: event.title, created: false, path: filePath });
      continue;
    }

    const code = generateEventScript(event);
    writeFileSync(filePath, code, 'utf-8');

    log.info({ name: event.name, title: event.title }, 'Event script deployed');
    results.push({ name: event.name, title: event.title, created: true, path: filePath });
  }

  const created = results.filter(r => r.created).length;

  log.info({ created, total: CUSTOM_EVENTS.length }, 'Custom events deployment complete');

  return {
    success: true,
    total: CUSTOM_EVENTS.length,
    created,
    skipped: CUSTOM_EVENTS.length - created,
    events: results,
    note: created > 0
      ? 'Event scripts written. Restart server to load them. Activate in-game via GM commands or em.scheduleAtTimestamp().'
      : 'All event scripts already deployed.',
  };
}

/**
 * Check deployment status of all custom events.
 */
export function getCustomEventStatus() {
  return {
    eventDir: EVENT_DIR,
    events: CUSTOM_EVENTS.map(event => {
      const filePath = join(EVENT_DIR, `${event.name}.js`);
      return {
        name: event.name,
        title: event.title,
        description: event.description,
        normalRate: event.normalRate,
        boostedRate: event.boostedRate,
        rateProperty: event.rateProperty,
        scriptExists: existsSync(filePath),
      };
    }),
  };
}

